package maahi_dec20;

public class Starwith_Endwith {

	public static void main(String[] args) {
		// it check given string startswith prefix text returns true or false
		String str ="https://gmail.com";
		System.out.println(str.startsWith("https://"));
		System.out.println(str.startsWith("ht"));
		System.out.println(str.startsWith("Ht"));
		
		//it check given string endswith suffix text and returns true or false
System.out.println(str.endsWith("com"));


	}

}
