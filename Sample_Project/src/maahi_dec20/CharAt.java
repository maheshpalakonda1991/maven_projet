package maahi_dec20;

public class CharAt {

	public static void main(String[] args) {
		// it returns character for given index number
		String str ="selenium framework";
System.out.println(str.charAt(6));
for(int i=0;i<str.length();i++ )
{
	System.out.println(str.charAt(i));
}
	}

	
}
