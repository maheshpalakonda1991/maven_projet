package maahi.dec6;

public class Data_types {

	public static void main(String[] args) {
		// integer type --used to store whole numbers
		byte age=40;
		short marks= 90;
		int salary = 700000;
		long turnover = 2000000;
		System.out.println("age is ="+age);
		System.out.println("marks are ="+marks);
		System.out.println("salary is ="+salary);
		System.out.println("turn over ="+turnover);

// fractional used to store whole numbers		
	float x=78.5385784f;
	double s=67.4887767d;
	System.out.println("float value is = "+x );
	System.out.println("double value is = "+s);
	// character type used to store single digit or alphabets or symbols 
	char gender = 'm';
	System.out.println("gender value is = " +gender);
	// conditional type used to return true or false
	boolean isworking = true;
	System.out.println("boolean value is = "+ isworking);
	// string it is not a datatype it is predefine class used to store sequence of character
	String str = "hello welcome to selenium";
	System.out.println("print string value = " +str);
	
}
	}
