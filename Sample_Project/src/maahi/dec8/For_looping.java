package maahi.dec8;

public class For_looping {

	public static void main(String[] args) {
		//print my name for 10 times
			
	      for(int i=1;i<=10;i++)
			{	
			System.out.println("maahi "+i);	
			}
		
	      System.out.println("========= ");
	      for(int i=1;i<=10;i+=2)
	      {
	    	  System.out.println("maahi "+i);  
	      }
	     
	      System.out.println ("========= ");
	      for(int i=10;i>=1;i--)
	      {
	    	  System.out.println ("maahi  "+i);
	      }
	      
	      System.out.println ("========= ");
	      for(int i=10;i>=1;i-=2)
	      {
	    	  System.out.println ("maahi  "+i);
	      }  
	      
	      
	      
	}

}

